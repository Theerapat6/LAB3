#FILO_
stack = ["A", "B", "C", "D", "E", "F"]
array = []

for i in stack:
    array.append(i)
print(array)

for i in stack:
    array.pop(0)
print(array)

#FIFO_
stack1 = ["A", "B", "C", "D", "E", "F"]

for i in stack1:
    array.append(i)
print(array)

while stack1:
    array.pop(0)
print(array)


